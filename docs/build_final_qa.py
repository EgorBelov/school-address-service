# ─── Хелперы для извлечения секций из исходников ─────────────────

def _find_heading_index(doc, text_prefix, style_name="HSETitle1"):
    """Найти индекс body-элемента: параграф со стилем style_name, текст
    которого начинается на text_prefix. Возвращает -1 если не найдено."""
    body = doc.element.body
    for i, child in enumerate(body):
        pStyle = child.find(qn("w:pPr") + "/" + qn("w:pStyle"))
        if pStyle is None or pStyle.get(qn("w:val")) != style_name:
            continue
        text = "".join(t.text or "" for t in child.iter(qn("w:t")))
        if text.startswith(text_prefix):
            return i
    return -1


_NS_A = "http://schemas.openxmlformats.org/drawingml/2006/main"
_NS_R = "http://schemas.openxmlformats.org/officeDocument/2006/relationships"


def _remap_image_rels(el, src_part, dst_part):
    """Перенести image-relationships для <a:blip> внутри скопированного
    элемента. Без этого Word не найдёт rId, на который ссылается тег
    <w:drawing>, и рисунок не отобразится. Создаёт новые relationship'ы
    в dst_part, указывающие на ImagePart из src_part (python-docx при
    сохранении сериализует их в новый docx-пакет)."""
    embed_attr = "{%s}embed" % _NS_R
    blip_tag = "{%s}blip" % _NS_A
    for blip in el.iter(blip_tag):
        old_rid = blip.get(embed_attr)
        if not old_rid or old_rid not in src_part.rels:
            continue
        src_rel = src_part.rels[old_rid]
        src_image_part = src_rel.target_part
        new_rid = dst_part.relate_to(src_image_part, src_rel.reltype)
        blip.set(embed_attr, new_rid)


def _copy_body_range(src_doc, dst_doc, start_idx, end_idx):
    """Копирует элементы body src_doc[start_idx:end_idx] (правая граница
    не включается) в dst_doc. Вставка делается ПЕРЕД <w:sectPr>
    (свойствами секции), чтобы порядок body оставался валидным —
    sectPr обязан быть последним. Свойства секции из источника не
    переносим: у dst_doc свои.

    Для каждого скопированного элемента переносим image-relationships
    (см. _remap_image_rels), иначе Word не отобразит рисунки."""
    src_body = list(src_doc.element.body)
    dst_body = dst_doc.element.body
    dst_sectPr = dst_body.find(qn("w:sectPr"))
    for el in src_body[start_idx:end_idx]:
        tag = el.tag.split("}")[-1]
        if tag == "sectPr":
            continue
        clone = deepcopy(el)
        _remap_image_rels(clone, src_doc.part, dst_doc.part)
        if dst_sectPr is not None:
            dst_sectPr.addprevious(clone)
        else:
            dst_body.append(clone)


def _add_part_divider(doc, part_label, part_subtitle):
    """Полностраничный разделитель Части. Page break идёт в НАЧАЛЕ
    (переводит на свежую страницу со страницы Введения / последней
    главы предыдущей части); в конце page break НЕ ставим — следующая
    глава начнётся с новой страницы автоматически через pageBreakBefore
    стиля HSE Title 1."""
    doc.add_page_break()

    # Несколько пустых параграфов, чтобы заголовок «ЧАСТЬ N» оказался
    # примерно в верхней трети страницы.
    for _ in range(8):
        doc.add_paragraph()

    p = doc.add_paragraph()
    p.paragraph_format.alignment = WD_ALIGN_PARAGRAPH.CENTER
    p.paragraph_format.line_spacing = LINE_SPACING
    run = p.add_run(part_label)
    _set_font(run, size=FONT_SIZE + 8, bold=True)  # 21pt

    doc.add_paragraph()

    p = doc.add_paragraph()
    p.paragraph_format.alignment = WD_ALIGN_PARAGRAPH.CENTER
    p.paragraph_format.line_spacing = LINE_SPACING
    run = p.add_run(part_subtitle)
    _set_font(run, size=FONT_SIZE + 4, bold=True)  # 17pt


