import re


# Все известные типы улиц/проездов в одном regex с границами слов.
# \b на Python 3 работает с unicode → корректно отделяет «ш» от «школа» и т.п.
_STREET_TYPE_RE = re.compile(
    r"\b("
    r"улица|ул\.?|"
    r"проспект|просп\.?|пр-кт|пр-т|пр\.?|"
    r"переулок|пер\.?|"
    r"проезд|пр-д|"
    r"бульвар|б-р|"
    r"шоссе|ш\.?|"
    r"набережная|наб\.?|"
    r"площадь|пл\.?|"
    r"тупик|туп\.?|"
    r"аллея|"
    r"линия|"
    r"тракт|"
    r"дорога|дор\.?|"
    r"квартал|кв-л|"
    r"микрорайон|мкр\.?|мкрн\.?"
    r")\b\.?",
    re.IGNORECASE,
)


def normalize_street_name(value: str | None) -> str:
    if not value:
        return ""

    value = value.lower().strip()
    value = _STREET_TYPE_RE.sub(" ", value)
    value = re.sub(r"\s+", " ", value)
    return value.strip()


def normalize_locality(value: str | None) -> str:
    """
    Нормализует название населённого пункта / муниципалитета.
    Приводит к единому виду:
      "Балашиха", "г. Балашиха", "город Балашиха",
      "Городской округ Балашиха", "г.о. Балашиха",
      "Московская обл, г Балашиха" → "балашиха".
    """
    if not value:
        return ""

    value = value.lower().strip()

    # Отбрасываем регион вида "Московская обл,", "Пермский край,"
    value = re.sub(
        r"^[а-яё\- ]+\s+(обл|область|край|респ|республика)[.,]?\s*",
        "",
        value,
    )

    replacements = [
        "городской округ",
        "город. округ",
        "г.о.",
        "г.о",
        "муниципальный округ",
        "муниципальный район",
        "поселение",
        "пгт",
        "посёлок",
        "поселок",
        "село",
        "деревня",
        "город",
        "г.",
        "пос.",
        "с.",
        "д.",
        "п.",
        "обл.",
        "обл",
    ]

    for item in replacements:
        value = value.replace(item, " ")

    value = re.sub(r"[,;]", " ", value)

    # Одиночные «г», «с», «д», «п» (без точки) — типа «г Балашиха», «п Сосновка».
    # Только окружённые пробелами / границами строки, чтобы не оторвать букву
    # от настоящего слова.
    value = re.sub(r"(^|\s)[гсдп](\s)", r"\1\2", value)

    value = re.sub(r"\s+", " ", value)

    return value.strip()
